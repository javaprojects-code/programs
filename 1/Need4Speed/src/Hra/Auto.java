package Hra;

public class Auto {
	private String jmeno;
	private String souradniceNaMape;
	
	public Auto(String jmeno, String souradniceNaMape) {
		super();
		this.jmeno = jmeno;
		this.souradniceNaMape = souradniceNaMape;
	}

	public String getJmeno() {
		return jmeno;
	}

	public void setJmeno(String jmeno) {
		this.jmeno = jmeno;
	}

	public String getSouradniceNaMape() {
		return souradniceNaMape;
	}

	public void setSouradniceNaMape(String souradniceNaMape) {
		this.souradniceNaMape = souradniceNaMape;
	}
	

}
